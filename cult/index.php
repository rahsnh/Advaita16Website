        <p>The musically bobbing island of Euphoria owes its existence to Emperor Tanzen (god of dance) 
and Empress Zamira (goddess of music). These two deities have taken care of the isle since its separation from the Ring of Fire. 
Due to the influence of the parent deities, the Euphorians have a culture rich in music, art and dance. Dance and Music has always been 
a ritual to appease the spirits governing the isle.</p>

        <p>Empress Zamira owns a splendid magical harp, that when played under the sun 
radiates shining musical energy all over the isle, from the mountains to the rivers, giving each Euphorian its share of energy and light.
This magical harp is the very source of survival of the Euphorians as their life-forces are replenished every day by the radiant beams; 
if not for the harp, the Euphorians would have been long extinct, as after the Great Rift their isle was unluckily positioned 
to get minimal sunshine.</p>

        <p>Every year the magic of the harp itself is to be renewed, which is possible 
only at the Ring of Fire. In springtime the Euphorians travel there to renew the reason of their existence; this time 
they are not the only ones arriving at the Ring of Fire.</p>

                <p>Seeing the sun turn green, all the islands had joined hands to tackle the menace of the Scavengers 
and restore their beloved sun to its former glory and resplendence. The relieved denizens of each island were now eager 
to be part of Advaita, the Grand Carnival at the Ring of Fire. The Euphorian musicians and dancers would enchant everyone 
with their art. Everyone in Euphoria is now excited to witness the beginning of the legend of Advaita.</p>
                
