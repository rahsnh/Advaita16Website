        <p>And so it all began a billion years ago....</p>

        <p>It was the day of the Athlympics, the Ring’s biggest sporting event. 
The humongous arena boasted of huge swimming pools, various courts, wrestling rings, skating rinks, and athletic tracks, 
besides housing huge restaurants and hotels for participants and  spectators alike. It was a mini planet in itself.  </p>

                <p>Nobody expected the occurrence of a huge explosion, which devastated the large stadium 
and separated the whole region from the Ring of Fire. The survivors were mostly sportsmen and a few spectators. 
When they came back to their senses, they found that they were floating in dark space, surrounded with asteroids, 
comets, meteoroids ... and weird aliens ! The first few days went without much work, since food and water was available 
but slowly the supplies reached the verge of depletion. Now their survival depended on only one option -  
to hunt down the diabolical beings floating into the remnants of their devastated dimension. But who would hunt them down?</p>
                <p>The shot-put and javelin throwers crafted a plan – the beasts shall be attracted and injured with the shot-put balls
 and later hunted down with the javelins; it worked. Then the chess grandmasters started synthing water from the hydrogen 
and the oxygen tanks in the debris. It took them many years, but life was finally restored .... to a new Athlon. </p>
<p>Present Time.</p>
                <p>It was time for the Athlympics again. Just as the Athlon Torch was lit to mark the beginning of the event, 
the sun took on a green hue. The dreaded tell-tale pirates, sun-raiders of the Apostle Galaxy, the Scavengers had attacked ! 
The Athlonites had no idea that they would witness an overwhelming Scavenger blitzkrieg !
The brave sportsmen rushed the women and children to safety, and all protective measures and defenses were adopted. 
But they knew they were no match for the mighty Scavengers.</p>
                <p>And suddenly an unthinkable miracle happened - the Scavengers started retreating to their galaxy! 
The Athlonites started celebrating, only to later know the real reason of the miracle - the heroics of the mighty Techeon 
and Shutter Island. They decided to transport the Athlympics to the Ring of Fire where the other islands too would meet 
after a long time. With the Athlympics, ADVAITA would become a never-before-seen spectacle in the history of the Ring of Fire.
</p>
