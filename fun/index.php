				<p>The residents of this island are not gifted with any magical powers. They don’t have any governing body. They are free thinkers and love being 
        independent and thus declared themselves as their own leaders. </p>

				<p>They believe that “Smile is the best way to face every problem, to hide every pain and to crush every fear.” Many of them lost their near and 
        dear ones in the catastrophe caused by the Great Rift, but they all stood determined and spirited. They put constant efforts to bring smiles on 
        each other’s face and live a cheerful and satisfied life. They kept themselves occupied with playing games, performing acts and celebrating. Years 
        passed and one fine morning appeared the guardian angel of peace and appreciated them for the unity and harmony they maintained among themselves and 
        as a regard with great respect christened their island as “Fun-o-Land”. The residents of this island became famous and are admired for boosting high 
        spirits within individuals by organising fun events and sharing glee.</p>

				<p>Little did the merrymakers know that their troubles had not ended. They were busy in preparation of the annual carnival when the sun turned green. 
        The smiles faded.The music stopped.It took seconds for the gloom to take over.They knew something terrible was going to happen. Was one Great Rift not 
        enough? The residents started preparing for doomsday.Everyone  returned to their families to say their goodbyes.The end  had come AGAIN.... </p>

        <p>But to their utter surprise,the sun turned fiery yellow again.It finished just like it had started,in a flash.The celebrations started again. It is 
        rightly said that nothing increases happiness like a surge of gloom. They decided to  have the carnival ,bigger and better.But how did it just go away? 
        No one cared about it in Fun-o-Land till they got Athelon's message about the heroics of Techiyon and Shutter Island. </p>
        
        <p>The grateful Fun-o-Landers decided to shift the carnival to the Ring of Fire and also to include the other islands.They too will travel to witness 
        the unprecedented ADVAITA...</p>
