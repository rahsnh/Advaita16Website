        <p>Billions of years ago, in a galaxy light years away, 
there existed the Ring of Fire – a group of massive islands, home to the most intelligent life forms 
of the universe. Some attribute its existence to the supernatural, claiming it to be a miracle. It is difficult 
to even comprehend how a chunk of land orbiting near a star could herald the most advanced species 
of the universe. The inhabitants had created a utopian civilization where people excelled in all fields - 
science, sports, literature - name it and you will find a Ring islander seated at the pinnacle.</p>

        <p>The Ring basically consisted of huge concentric 
land masses separated by moats and linked by canals flowing with super-dense space. 
At the centre of the Ring stood a giant dormant volcano. Each of the six landmasses of the Ring 
was called an Isle. People from each Isle excelled in one of many vocations - 
The Techiodes from Techeon Isle were the pioneers of science and technology; Athlon trained 
star sportsmen; Shutter Island was home to the Shutterbugs, the most creative people of the Ring 
who were also master film-makers and photographers; Euphoria enriched minds with artistes and 
exponents of music and dance, while Erudite was the land of genesis of great scholars of literature 
and extempore. Besides, there were the cheerful Merrymakers from Funola, whose main aim in life 
was to be happy and make merry.</p>

        <p>Everything was going good till an energy crisis hit the Ring. 
A civilization as big and advanced as the Ring requires a lot of energy. 
The Islanders had every technology of energy production perfected, but with great prosperity comes great energy demands. 
They went on innovating and engineering new ways to harness energy 
from various sources. But they knew that they were going to hit rock bottom.</p>

                <p>It was high time when they recognized the mounting energy signatures 
from the dormant volcano. They very well understood the risk of encountering 
a deadly volcanic explosion. But looking at the pressures of the rising energy crises, 
the Isles decided to have a go at it. They sent their best miners and engineers to work. 
But Murphy's Law came into action again. The dormant volcano erupted, causing 
massive earthquakes and shockwaves that flung each Isle far away. 
Millions of lives were lost. Whole landscapes were wiped out. In mere seconds, 
every accomplishment of theirs was reduced to rubble.</p>
                <p>The Isles blamed each other for what was termed as The Great Rift. 
They decided to never meet, and all lines of communication were severed. 
The Ring of Fire was now just the dormant volcano and the surrounding landmass, 
formed from the solidified plasma erupted from the volcanic cone. 
The Isles were now six distant, disconnected entities floating in empty space, each island considering the others as inexistent.</p>
<p>As time passed, each island became the master in their 
respective vocations. However, the idea of resuming 
inter-island communication was limited to books, debates 
and documentaries, even a billion years after the Great Rift.</p>
                <p>Everything changed when the sun turned green....</p>
                
