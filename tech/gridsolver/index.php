                          <h3>Round 1</h3>
                               <ul>
                                  <li><span class="fa fa-space-shuttle" ></span> The bot will start from starting zone as shown in the figure and move towards north.</li>
                                  <li><span class="fa fa-space-shuttle" ></span> The bot has to move forward until it reaches the coordinate (3, 5) or any obstacle is detected.</li>
                                  <li><span class="fa fa-space-shuttle" ></span> If obstacle is detected then set a flag say ‘obs’ as 1. If no obstacle is detected and the bot
 reaches the node (3, 5) the set ‘obs’ as 0. The obstacle will never be present on node (3, 0).</li>
                                  <li><span class="fa fa-space-shuttle" ></span> A variable say ‘p’ stores number of nodes travelled on this path. If the obstacle was present on
 node (3, 4) then ‘p’ would store 4 and if no obstacle was detected then ‘p’ would be 5.</li>
                              
                                  <li><span class="fa fa-space-shuttle"></span> Let say the current coordinate is (x, y), then if x<y move west by 1 node, x>y move east by 1
 node, x=y move east by 2 nodes.</li>
                                  <li><span class="fa fa-space-shuttle"></span> Let say the present coordinate now is (a, b) then from here the bot has to travel to node with
 the coordinate given by this formula: [(a+obs-floor(p/2), floor((b+p)/2)+floor(a/2))].</li>
                                  <li><span class="fa fa-space-shuttle"></span> Now the bot has to travel to the node exactly symmetrical to current node along the axis shown
 in the figure. If the bot is present on the axis then travel to 2 nodes nearest to you on the axis
 one by one.</li>
                                  <li><span class="fa fa-space-shuttle"></span> Now come out of the grid through the start position.</li>
                                  <li><span class="fa fa-space-shuttle"></span> There might be other obstacles all over the grid. So the bot must be careful and must find
 another path while travelling to a node.</li>
