        <p>One fine morning, Mr. Steve McCurry, 
focusing hard on his camera, attempted to get a perfect snapshot of the island’s 
most beautiful monument - The Shutter Rock. Existing from before the Great Rift, 
the Rock was rumored to be as old as the Ring of Fire itself. But nobody knew 
how the giant shutter shaped rock came into being. The shutter in front of the rock 
was clearly unnatural, with engineers claiming it to possess moving parts and 
inexplicable mechanisms. Nobody knew what Mr. McCurry knew...</p>

        <p>Mr. McCurry used to start his day 
with the view of the rock, for it was a gift to him from his father, 
the late King Ansel Adams. After the old king’s death, few were convinced 
that little Steve could handle the responsibility. But his very first public 
documentary instilled belief and confidence in the citizens. After that, 
he never looked back. The island has flourished under the young new king, 
and their photo and film output per year increased manifold. Steve had no idea 
that the last day of his new film-shoot would be unusually busier 
than any other before …
</p>

        <p>It all began when the sun turned green. 
The Shutterbugs witnessed several Techeon and Scavenger aircrafts crowding their skies 
and thousands of missiles being fired. But they were no worthy match for 
the savage rampaging Scavengers.</p>

                <p>Understanding the responsibility of a king, Mr. McCurry immediately flung into action. 
Firstly, he ordered a squadron to barricade Shutter Island and engage the enemies. 
Then, the dwellers of the island were controlled and given assurance and courage. 
The polaroidamine, a gigantic device that flashes polarized light on the enemy and stuns them, 
was brought into action.</p>
<p>But the Scavengers were found to be faster and sturdier. Just like in Techeon’s case, 
the Shutterbugs’ attempts were turning futile. The scavengers proved to be very powerful. 
Shockingly, their suicidal bombings served only to multiply their numbers 
with every explosive collision. The situation worsened; there was chaos everywhere.</p>
                <p>With a heavy heart, Mr. McCurry decided to bring the Shutter Rock 
into application and unleash its secret, fiercely guarded for several years. 
The rock was in reality a paranormal device to be used only once in ten millennia. 
He went to his high-security vault ‘No. 687’ and took out the Control Key. 
Once the shutter of the rock was opened, it could generate a perfect 
three-dimensional holographic illusion, perceived by all the senses 
including the eyes.</p>
<p>The technology in Shutter Island has just touched the pinnacle 
of holographic film making; to reach the stage of the Shutter Rock 
would take them a million years more. The cosmos was in danger. 
It was the high time for the application of the magical device. 
But to use the device they needed a distraction, and 
Shutter Island lacked the needful resources. They could not ask for help. 
So many years had passed since the Isles' last show of unity. 
But King McCurry had no other way. He opened all communication lines, 
and voila! Techeon acknowledged their distress call, and together they devised a stratagem.</p>
                <p>Techeon would launch a full blown attack at the Scavengers, 
while the Shutter Rock would form an illusion of a Sun in the Apostle galaxy, 
now visible due to pan-dimensional rifts caused by the Scavengers’ suicide bombings. 
Due to the Shutter Rock, the Apostle sun would be so real that no one could tell the difference. 
They could even sense the heat and light.</p>
                <p>The plan was successful. The Scavengers were both routed 
as well as lured (by the make-believe sun) back to their galaxy. A new colossal 
display of unison had brought the Isles a victory like never before. 
To celebrate this, they decided to go back to the Ring of Fire. 
A word was sent to other islands too. The script writers 
of Shutter Island named it Advaita – ‘without dual’, ‘unparalleled’. 
After all it would be the biggest carnival in the Ring's history....</p>
            
