        <p>Seated on a solid confinement of plasma that hovered over a giant magnetic bar, 
Mr. Leonard Newburg sipped his morning cup of coffee, pondering over the work to be done 
in the Techeon Parliament today - from passing a few legislator bills, to starting the functioning 
of the radiation protection umbrella in the housing societies, attending a conference on 
combating terrorism, with the Isle of Athlon, and inaugurating a swimming pool in 
the snake ring corner (not just any pool, but an enormous one holding super fluids 
at would enable the pool to hover some 200 feet above mean ground level).</p>

        <p>“Just another busy day …” breathed Mr. Newburg; little did he know what was in store for him …</p>

        <p>Mr. Leonard Newburg has served as President of Techeon Isle 
for the past 2 million years. Born to a human mother and an android father, 
he was technically a cyborg. A hardworking, trustworthy and amicable man, 
he held knowledge of the past and present of Techeon on his fingertips; 
his flawless personality and presidential skills ensured that he was never voted out of power.</p>

                <p>After finishing his hot coffee, Mr. Leonard Newburg teleported to his office. 
He then sat down to complete the day’s work. He passed his pending bills, 
activated the radiation protection umbrella, and inaugurated the swimming pool. 
But, just stepping into the conference room, he beheld a most terrifying sight – the golden sun turned green!</p>
<p>All the people came out of their houses wondering what was going on. It was not an eclipse - 
the sun was being hijacked by the most dreaded terrorist group, The Scavengers, from the Apostle galaxy! 
Their planet orbited no sun of its own, so these space pirates captured stellar essence from the suns 
of other solar systems. A chemical that switched off solar fusion aided the Scavengers in shipping off 
whole stars to their galaxy, and there they would feast on the harvested energy until they needed 
to harness anew. Nobody had the courage to fight against these dreadnoughts.</p>
                <p>Mr. Newburg immediately sprang into action. The radiation protection umbrellas were first armed. 
The Artemis Vanguard units launched fission cluster-cores on the invaders, 
while the airborne Marshalls piloted giant Mark-15 warships towards the multitude 
of Scavenger drones. Solstine, a chemical for solar revival was projected 
into the sun’s chromosphere.</p>
<p>Nothing worked. The suicide bombers of the Scavengers projected themselves 
at light-speed, and the resulting explosions produced over a billion megatons 
of energy, enough to put supernovae to shame. 
The whole Island of Techeon was in chaos. People were suffering.</p>
                <p>Just at the brink of total despair, Shutter Island established communication links with Techeon. 
Mr. Newburg realized that collaboration was the only way out. The king of the Shutter Island 
told them about their secret implement - Shutter Rock. Mr. Newburg could not believe his ears 
when he heard what the Rock could do. Trusting the Shutter-Islanders was a huge leap of faith. 
But he had run out of options. The Techeon strategists instantly joined hands with Shutter Island 
and charted a plan - the good old distract-and-hit-hard strategy.</p>
                <p>The whole island of Techeon was on fire, the other islands were helping as well 
but without much success. The Techeon then collected all their nuclear weapons and decided 
to go all out at the Scavengers. They knew they could hold them only for a few minutes, 
but the risk was worth it. Their counter-attack started … and with it started the ‘Illusion’, 
which the Scavengers failed to decode. Busy reducing the Techeon warships to rubble, 
the Scavengers realized too late that the strategists had decided to divert half their forces 
to attack from back. The plan worked like magic; the Scavengers could not believe their optics. 
There was no possible way it could have happened. But the Illusion was too strong for 
even their surveillance craft to differentiate. 
The war finally ended after a year of tiring strife and devastation.</p>
                <p>It took seven weeks for the sun to shine anew, and life was restored to normalcy. 
Mr. Leonard heaved a sigh of relief. He immediately ordered making a force field around the planet 
to prevent any further attacks from the scavengers. For now, it was time for some celebration.</p>
                <p>They agreed to the proposal of Advaita put forward by the Shutter Island 
and immediately relayed the messages to the other islands. The Techeon islanders, 
or Techiodes, decided to showcase their latest scientific exhibits in the carnival. 
Their fellow islanders would definitely be awestruck with their inventions. 
It was the beginning of a new age for the Ring - the age of unity.</p>
